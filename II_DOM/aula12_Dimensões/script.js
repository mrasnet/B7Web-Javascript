/*  #M2  aula #12: Dimensões

    offsetHeight       - retorna a altura do elemento, considerando além do height, padding, border, scrollbar (em alguns navegadores)
    offsetWidth        - retorna a largura do elemento, considerando além do width, padding, border, scrollbar (em alguns navegadores)
    
    clientWidth        - parecido com o de cima, entretanto desconsidera a barra de rolagem (scrollbar) e a borda
    clientHeight       - retorna a largura + padding 

    scrollHeight       - retorna a altura considerando padding, conteúdo e espaço disponível para o conteúdo
    scrollWidth        - retorna a largura considerando padding, conteúdo e espaço disponível para o conteúdo

    ex.: document.querySelector('.texto').offsetHeight
*/
