/* --------------------- #M5 aula #2: O que é JSON ----------------------- 

    Javascript Object Notation - JSON: 
    
    É uma sintaxe de objeto - um objeto javascript utilizado para fazer a comunição entre requisições, tanto para enviar como receber informações
    
    Para criar um JSON definimos uma variavel com um objeto onde nele iremos guardar as nossas informações - nome do dado e valor 
    Podemos ter objetos que definem tipos primitivos, com referencia para arrays ou outros objetos.
*/

